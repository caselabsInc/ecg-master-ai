type RichBlock =
  | { type: 'paragraph'; text: string }
  | { type: 'heading'; title: string; text?: string }
  | { type: 'numbered'; number: string; title?: string; text: string };

function cleanMarkdown(text: string) {
  return text.replace(/\*\*/g, '').replace(/\*/g, '').replace(/\s+/g, ' ').trim();
}

function splitInlineBulletSections(block: string) {
  const bulletParts = block
    .replace(/\s+-\s+(?=[A-Z0-9][^:]{1,90}:)/g, '\n- ')
    .split(/\n-\s+|^-\s+/)
    .map((part) => part.trim())
    .filter(Boolean);

  if (bulletParts.length <= 1 && !block.trim().startsWith('-')) return null;

  return bulletParts.map<RichBlock>((part) => {
    const headingMatch = part.match(/^([^:]{2,100}):\s*([\s\S]*)$/);

    if (headingMatch) {
      return {
        type: 'heading',
        title: cleanMarkdown(headingMatch[1]),
        text: cleanMarkdown(headingMatch[2]),
      };
    }

    return { type: 'paragraph', text: cleanMarkdown(part) };
  });
}

function displayUrgencyLabel(urgencyLevel: string, hasAiDraft: boolean) {
  if (!hasAiDraft) return 'Not available';
  if (!urgencyLevel || urgencyLevel === 'insufficient_data') return 'Interpretation available';
  return urgencyLevel.replaceAll('_', ' ');
}

function displayInterpretationStatus(status?: string, hasAiDraft = false) {
  if (hasAiDraft || status === 'generated') return 'Available';
  if (status === 'failed') return 'Failed';
  return 'Not requested';
}

function previewText(text: string, maxLength = 180) {
  const firstBlock = cleanMarkdown((text || '').split(/\n{2,}/)[0] || '');
  if (firstBlock.length <= maxLength) return firstBlock;
  return `${firstBlock.slice(0, maxLength).trim()}...`;
}

function isQrsFinding(finding: NonNullable<NonNullable<EcgReport['decisionSupport']>['ruleFindings']>[number]) {
  return finding.id === 'qrs-complex';
}

function splitInputs(inputs: string[]) {
  return inputs
    .map((input) => {
      const [label, ...rest] = input.split(':');
      return {
        label: label.trim(),
        value: rest.join(':').trim(),
      };
    })
    .filter(({ value }) => value && value !== 'not recorded' && value !== 'not documented');
}

function splitSummary(summary: string) {
  return summary
    .split(';')
    .map((item) => item.trim())
    .filter(Boolean);
}

function parseRichText(text: string): RichBlock[] {
  const normalized = (text || '').replace(/\r/g, '').trim();
  if (!normalized) return [];

  const rawBlocks = normalized
    .split(/\n{2,}/)
    .map((block) => block.trim())
    .filter(Boolean);

  return rawBlocks.flatMap<RichBlock>((block) => {
    const bulletSections = splitInlineBulletSections(block);
    if (bulletSections) return bulletSections;

    const numberedMatches = Array.from(
      block.matchAll(/(?:^|\n|\s)(\d+)\.\s+\*\*([^:*]+):\*\*\s*([\s\S]*?)(?=(?:\n|\s)\d+\.\s+\*\*[^:*]+:\*\*|$)/g)
    );

    if (numberedMatches.length) {
      return numberedMatches.map((match) => ({
        type: 'numbered' as const,
        number: match[1],
        title: cleanMarkdown(match[2]),
        text: cleanMarkdown(match[3]),
      }));
    }

    const headingMatch = block.match(/^\*\*([^:*]+):\*\*\s*([\s\S]*)$/);
    if (headingMatch) {
      return [
        {
          type: 'heading' as const,
          title: cleanMarkdown(headingMatch[1]),
          text: cleanMarkdown(headingMatch[2]),
        },
      ];
    }

    return [{ type: 'paragraph' as const, text: cleanMarkdown(block) }];
  });
}

function RichBlocks({
  blocks,
  tone = 'default',
  showNumbers = true,
}: {
  blocks: RichBlock[];
  tone?: 'default' | 'warning' | 'success';
  showNumbers?: boolean;
}) {
  const paragraphStyle = tone === 'warning' ? styles.richWarningParagraph : tone === 'success' ? styles.richSuccessParagraph : styles.richParagraph;

  return (
    <View style={styles.richText}>
      {blocks.map((block, index) => {
        if (block.type === 'heading') {
          return (
            <View key={`${block.title}-${index}`} style={[styles.richBlock, styles.richBlockSection]}>
              <Text style={styles.richHeading} selectable>
                {block.title}
              </Text>
              {block.text ? (
                <Text style={paragraphStyle} selectable>
                  {block.text}
                </Text>
              ) : null}
            </View>
          );
        }

        if (block.type === 'numbered') {
          if (!showNumbers) {
            return (
              <View key={`${block.number}-${block.title}-${index}`} style={styles.richBlock}>
                {block.title ? (
                  <Text style={styles.richHeading} selectable>
                    {block.title}
                  </Text>
                ) : null}
                <Text style={paragraphStyle} selectable>
                  {block.text}
                </Text>
              </View>
            );
          }

          return (
            <View key={`${block.number}-${block.title}-${index}`} style={styles.numberedRow}>
              <View style={styles.numberBadge}>
                <Text style={styles.numberBadgeText}>{block.number}</Text>
              </View>
              <View style={styles.numberedCopy}>
                {block.title ? (
                  <Text style={styles.richHeading} selectable>
                    {block.title}
                  </Text>
                ) : null}
                <Text style={paragraphStyle} selectable>
                  {block.text}
                </Text>
              </View>
            </View>
          );
        }

        return (
          <Text key={`${block.text}-${index}`} style={paragraphStyle} selectable>
            {block.text}
          </Text>
        );
      })}
    </View>
  );
}

function RichText({ text, tone = 'default' }: { text: string; tone?: 'default' | 'warning' | 'success' }) {
  const blocks = parseRichText(text);

  return <RichBlocks blocks={blocks} tone={tone} />;
}

function tabTitleForBlock(block: RichBlock, index: number) {
  if (block.type === 'numbered') return block.title || `Step ${block.number}`;
  if (block.type === 'heading') return block.title;
  return `Item ${index + 1}`;
}

function TabbedRichText({ text, fallbackLabel = 'Details' }: { text: string; fallbackLabel?: string }) {
  const blocks = parseRichText(text);
  const tabBlocks = blocks.length > 1 ? blocks.map((block) => [block]) : [blocks];
  const [activeIndex, setActiveIndex] = useState(0);

  if (!blocks.length) return null;
  if (tabBlocks.length <= 1) return <RichBlocks blocks={blocks} />;

  const safeIndex = Math.min(activeIndex, tabBlocks.length - 1);

  return (
    <View style={styles.innerTabsContainer}>
      <ScrollView horizontal showsHorizontalScrollIndicator={false} contentContainerStyle={styles.innerTabs}>
        {tabBlocks.map(([block], index) => {
          const active = safeIndex === index;
          return (
            <Pressable key={`${tabTitleForBlock(block, index)}-${index}`} style={[styles.innerTab, active && styles.innerTabActive]} onPress={() => setActiveIndex(index)}>
              <Text style={[styles.innerTabText, active && styles.innerTabTextActive]} numberOfLines={1}>
                {tabTitleForBlock(block, index) || fallbackLabel}
              </Text>
            </Pressable>
          );
        })}
      </ScrollView>
      <View style={styles.innerTabPanel}>
        <RichBlocks blocks={tabBlocks[safeIndex]} showNumbers={false} />
      </View>
    </View>
  );
}

