function formatReportDate(report: EcgReport) {
  const value = report.createdAt?.toDate?.() || report.context?.ecgDateTime?.toDate?.();
  if (!value) return 'Date not documented';

  return value.toLocaleDateString(undefined, {
    day: 'numeric',
    month: 'short',
    year: 'numeric',
  });
}

function readInterpretation(ai: EcgReport['aiInterpretation'] | any) {
  const normalized = normalizeAiInterpretation(ai);
  if (normalized) {
    return {
      primary: normalized.primaryInterpretation,
      dangerousFindings: normalized.dangerousFindings,
      missingDataWarnings: normalized.missingDataWarnings,
      urgencyLevel: normalized.urgencyLevel,
      teaching: normalized.teachingExplanation,
      summary: normalized.stepByStepInterpretation,
      differentials: normalized.differentialsAndExplanation,
      investigations: normalized.nextSteps.investigations,
      management: normalized.nextSteps.management,
      notes: normalized.teachingExplanation,
      disclaimer: normalized.disclaimer,
    };
  }

  return {
    primary:
      ai?.primaryInterpretation ||
      ai?.summary ||
      'Interpretation support is not available for this report yet.',
    dangerousFindings: ai?.dangerousFindings || 'No dangerous-finding assessment was provided.',
    missingDataWarnings: ai?.missingDataWarnings || 'No missing-data concerns were provided.',
    urgencyLevel: ai?.urgencyLevel || 'insufficient_data',
    teaching:
      ai?.teachingExplanation ||
      ai?.educationalNotes,
    summary:
      ai?.stepByStepInterpretation ||
      ai?.summary ||
      'Interpretation support is not available for this report yet.',
    differentials:
      ai?.differentialsAndExplanation ||
      ai?.possibleFindings ||
      'No differential considerations were provided.',
    investigations: ai?.nextSteps?.investigations || 'No investigation suggestions were provided.',
    management: ai?.nextSteps?.management || 'No management considerations were provided.',
    notes: ai?.educationalNotes,
    disclaimer:
      ai?.disclaimer ||
      'This interpretation is generated by an AI model for educational purposes only. It does not constitute medical advice, diagnosis, or treatment.',
  };
}

function classifyDangerousFindings(text: string, urgencyLevel?: string): DangerousFindingsState {
  const normalizedText = text.toLowerCase();
  const noDangerPhrases = [
    'no dangerous findings',
    'no dangerous-finding',
    'no dangerous finding',
    'no acute dangerous',
    'no high-risk',
    'no immediately dangerous',
    'no life-threatening',
    'not identified',
    'none identified',
  ];
  const warningPhrases = [
    'stemi',
    'omi',
    'ventricular tachycardia',
    'complete heart block',
    'high-grade',
    'mobitz ii',
    'torsades',
    'hyperkalemia',
    'emergency',
    'urgent',
    'dangerous',
    'critical',
  ];

  if (noDangerPhrases.some((phrase) => normalizedText.includes(phrase))) return 'none';
  if (urgencyLevel === 'normal_variant' || urgencyLevel === 'routine') return 'none';
  if (warningPhrases.some((phrase) => normalizedText.includes(phrase))) return 'warning';
  return 'unknown';
}

